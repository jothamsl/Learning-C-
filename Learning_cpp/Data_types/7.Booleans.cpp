#include <iostream>
using namespace std;

int main(void)
{
    // BOOLEAN TYPE
    /* The integer value stored in a. 'char' is interpreted as a character
       while the integer stored in type 'bool' is interpreted as a logical 
       value (0 or 1), (false, true). 
    */

    // BOOLEAN LITERALS

    // Define variables
    bool x = 123;
    bool y = -8;
    bool z = 0;
    bool t = -0;
    bool u = true;
    bool v = false;

    // Outputting values
    cout << "Value of x: " << x << endl;
    cout << "value of y: " << y << endl;
    cout << "value of z: " << z << endl;
    cout << "Value of t: " << t << endl;
    cout << "Value of u: " << u << endl;
    cout << "Value of v: " << v << endl;
    return 0;
}